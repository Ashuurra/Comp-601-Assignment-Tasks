package Task6;
// Vegetable class
public class Vegetable {
	// Private fields
    private double weight;
    private double price;


    // Constructor that sets the fields
    public Vegetable(double weight, double price) {
        this.weight = weight;
        this.price = price;
    }


    // Getter for weight
    public double getWeight() {
        return weight;
    }


    // Setter for weight
    public void setWeight(double weight) {
        this.weight = weight;
    }


    // Getter for price
    public double getPrice() {
        return price;
    }


    // Setter for price
    public void setPrice(double price) {
        this.price = price;
    }
}
// End of Vegetable class